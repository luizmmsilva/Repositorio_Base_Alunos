# Crie um programa que verifique se um número é par ou impar usando o operador de módulo(%)
# Resultado do número 

# Etapas
# 1) Solicitar um numero ao usuário
# 2) verificar se é par ou impar
# #) Informe o resultado ao usuário

numero =int(input("Digite o primeiro número"))

if numero % 2 == 0:
    print("numero é par.")
else:
    print("numero é impar")