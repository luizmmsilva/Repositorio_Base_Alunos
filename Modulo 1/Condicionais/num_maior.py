# Crie um programa que leia o maior número entre dois números 

# Etapas 
# 1) Solicitar ao usuário 2 números
num1= float(input("DIgite um número: "))
num2= float(input("Digite um novo número: "))


# 2) Verificar a condicional  
# 3) Apresentar o resultado para o usuário
if num1 >= num2:
    print(f"O {num1} é maior que o {num2}. ")
else: 
    print(f"O {num2} é maior que o {num1}. ")


